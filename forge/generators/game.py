class GameGenerator:
    """
    Generates a complete mini-game package.

    Parameters
    ----------
    palette : Palette
    seed    : int
    output_dir : str
        Root directory for generated assets and scripts.
    """

    def __init__(self, palette: Palette, seed: int = 0,
                 output_dir: str = "generated_assets"):
        self.palette    = palette
        self._rng       = random.Random(seed)
        self._seed      = seed
        self.output_dir = output_dir

    # ------------------------------------------------------------------
    def generate(
        self,
        title:        str = "VoxelForge Game",
        genre:        str = "village",
        theme:        str = "",
        player_class: str = "warrior",
        enemies:      int = 3,
        props:        int = 6,
        level_size:   int = 48,
    ) -> Dict[str, Any]:
        """
        Generate a complete mini-game.

        Returns a manifest dict with all paths and a run_command.
        """
        g       = _GENRES.get(genre, _GENRES["village"])
        slug    = title.lower().replace(" ", "_")[:20]
        game_dir = os.path.join(self.output_dir, "games", slug)

        assets_dir  = os.path.join(game_dir, "assets")
        scripts_dir = os.path.join(game_dir, "scripts")
        scenes_dir  = os.path.join(game_dir, "scenes")
        for d in (assets_dir, scripts_dir, scenes_dir):
            os.makedirs(d, exist_ok=True)

        manifest: Dict[str, Any] = {
            "title":    title,
            "genre":    genre,
            "theme":    theme or g.get("biome", "grassland"),
            "seed":     self._seed,
            "assets":   [],
            "scripts":  [],
        }

        scene = Scene(background_color=g["bg_color"])

        # --- Level geometry ---
        level_path = self._generate_level(
            game_dir, assets_dir, g, theme or g.get("biome", "grassland"),
            level_size, manifest
        )
        scene.add_voxel_model("level", level_path, position=(0, 0, 0),
                               is_static=True)
        surface_z = 14.0

        # --- Player ---
        cgen   = CharacterGenerator(self.palette, seed=self._seed + 100)
        p_model = cgen.generate(
            class_type = player_class, skin_tone="tan", hair_color="brown",
            armour     = "plate" if player_class == "warrior" else "mage",
            weapon     = "sword" if player_class == "warrior" else "staff",
            name       = "player",
        )
        p_path = os.path.join(assets_dir, "player.vox")
        p_model.save(p_path)
        manifest["assets"].append({"name": "player", "path": p_path, "type": "character"})
        player_entity = scene.add_voxel_model(
            "player", p_path,
            position=(float(level_size // 2), float(level_size // 2), surface_z),
            is_static=False, add_rigidbody=True,
        )

        # --- Player script ---
        p_script = _player_script(player_class, genre)
        p_script_path = os.path.join(scripts_dir, "player.lua")
        with open(p_script_path, "w") as f:
            f.write(p_script)
        manifest["scripts"].append({"name": "player", "path": p_script_path})
        player_entity["LuaScript"] = {
            "scriptPath": scripts_dir, "scriptName": "player.lua"
        }

        # --- Enemies ---
        e_class  = g["enemy_class"]
        e_armour = g["enemy_armour"]
        e_weapon = g["enemy_weapon"]
        for i in range(enemies):
            e_model = CharacterGenerator(self.palette, seed=self._seed + 200 + i).generate(
                class_type = e_class, skin_tone="dark", hair_color="black",
                armour=e_armour, weapon=e_weapon, name=f"enemy_{i}",
            )
            e_path = os.path.join(assets_dir, f"enemy_{i}.vox")
            e_model.save(e_path)
            manifest["assets"].append({"name": f"enemy_{i}", "path": e_path, "type": "character"})

            ex = float(self._rng.randint(4, level_size - 8))
            ey = float(self._rng.randint(4, level_size - 8))
            e_entity = scene.add_voxel_model(
                f"enemy_{i}", e_path,
                position=(ex, ey, surface_z),
                is_static=False, add_rigidbody=True,
            )
            e_script = _enemy_script(e_class, genre, i)
            e_script_path = os.path.join(scripts_dir, f"enemy_{i}.lua")
            with open(e_script_path, "w") as f:
                f.write(e_script)
            manifest["scripts"].append({"name": f"enemy_{i}", "path": e_script_path})
            e_entity["LuaScript"] = {
                "scriptPath": scripts_dir, "scriptName": f"enemy_{i}.lua"
            }

        # --- Props (some are chests = collectibles) ---
        prop_types = g.get("prop_mix", ["tree", "crate", "chest", "rock"])
        pgen       = PropGenerator(self.palette, seed=self._seed + 300)
        chest_count = 0

        for i in range(props):
            ptype = prop_types[i % len(prop_types)]
            p_name = f"prop_{ptype}_{i}"
            p_model = pgen.generate(ptype, name=p_name)
            pp_path = os.path.join(assets_dir, f"{p_name}.vox")
            p_model.save(pp_path)
            manifest["assets"].append({"name": p_name, "path": pp_path, "type": "prop"})
            px = float(self._rng.randint(2, level_size - 4))
            py = float(self._rng.randint(2, level_size - 4))
            scene.add_voxel_model(
                p_name, pp_path,
                position=(px, py, surface_z),
                is_static=True,
            )
            if ptype == "chest":
                chest_count += 1

        # --- Objective script ---
        obj_script = _objective_script(genre, chest_count)
        obj_path   = os.path.join(scripts_dir, "objective.lua")
        with open(obj_path, "w") as f:
            f.write(obj_script)
        manifest["scripts"].append({"name": "objective", "path": obj_path})
        # Attach to first entity (world entity)
        if scene._entities:
            scene._entities[0]["LuaScript"] = {
                "scriptPath": scripts_dir, "scriptName": "objective.lua"
            }

        # --- Ambient light ---
        scene.add_point_light("sun",
            position=(float(level_size//2), float(level_size//2), 50.0),
            color=(1.0, 0.95, 0.85), intensity=2.0,
            range_=float(level_size * 3))

        # --- Save scene ---
        scene_path = os.path.join(scenes_dir, f"{slug}.scene")
        scene.save(scene_path)
        manifest["scene_path"] = scene_path

        # --- Manifest JSON ---
        manifest_path = os.path.join(game_dir, "manifest.json")
        with open(manifest_path, "w") as f:
            json.dump(manifest, f, indent=2)
        manifest["manifest_path"] = manifest_path
        manifest["entity_count"]  = scene.entity_count
        manifest["run_command"]   = (
            f"cd engine && ./voxelforge --scene ../{scene_path}"
        )

        return manifest

    # ------------------------------------------------------------------
    def _generate_level(
        self, game_dir: str, assets_dir: str,
        g: Dict[str, Any], theme: str,
        size: int, manifest: Dict[str, Any],
    ) -> str:
        if g["level_type"] == "dungeon":
            dgen  = DungeonGenerator(self.palette, seed=self._seed)
            model = dgen.generate(size, size, wall_height=3,
                                   style=theme if theme in ("stone","dungeon","cave","ice") else "dungeon")
        else:
            tgen  = TerrainGenerator(self.palette, seed=self._seed)
            model = tgen.generate(size, size, max_depth=14, biome=g["biome"])

        path = os.path.join(assets_dir, "level.vox")
        model.save(path)
        manifest["assets"].append({"name": "level", "path": path, "type": "level"})
        return path
