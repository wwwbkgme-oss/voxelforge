def _player_script(player_class: str, genre: str) -> str:
    speed = 14.0 if player_class in ("rogue", "archer") else 10.0
    jump  = 9.0  if player_class in ("warrior", "mage") else 7.0
    return f"""--[[
  VoxelForge Generated Player Controller
  Genre: {genre}  Class: {player_class}
  Auto-generated by VoxelForge GameGenerator
--]]

local SPEED = {speed}
local JUMP  = {jump}
local score = 0
local health = 100
local max_health = 100

function Start()
    print("[Player] {player_class.title()} ready!")
    if not RigidBody.HasRigidBody(self) then
        RigidBody.AddRigidBody(self)
    end
    RigidBody.SetMass(self, 1.0)
    RigidBody.SetUseGravity(self, true)
    RigidBody.SetIsStatic(self, false)
end

function Update()
    local dt = Time.DeltaTime()
    local vel = RigidBody.GetVelocity(self)
    local dx, dy = 0, 0

    if Input.GetKey("W") or Input.GetKey("Up")    then dy =  SPEED end
    if Input.GetKey("S") or Input.GetKey("Down")  then dy = -SPEED end
    if Input.GetKey("A") or Input.GetKey("Left")  then dx = -SPEED end
    if Input.GetKey("D") or Input.GetKey("Right") then dx =  SPEED end

    RigidBody.SetVelocity(self, dx, dy, vel.z)

    if Input.GetKeyDown("Space") then
        local pos = Transform.GetPosition(self)
        if pos.z <= 2.0 then
            RigidBody.SetVelocity(self, dx, dy, JUMP)
        end
    end

    -- Camera follow
    local pos = Transform.GetPosition(self)
    Camera.SetPosition(pos.x - 30, pos.y - 30, pos.z + 50)

    -- HUD
    UI.DrawText("HP: " .. health .. "/" .. max_health, 20, 20, 0.2, 1, 0.2, 1)
    UI.DrawText("Score: " .. score, 20, 50, 1, 1, 1, 1)

    if Input.GetKeyDown("Escape") then
        Engine.ExitGame()
    end
end

function TakeDamage(amount)
    health = health - (amount or 10)
    if health <= 0 then
        print("[Player] Game over! Final score: " .. score)
        health = 0
    end
end

function AddScore(points)
    score = score + (points or 10)
end

function End()
    print("[Player] Session ended. Score: " .. score)
end
"""


def _enemy_script(enemy_class: str, genre: str, index: int) -> str:
    """Build enemy Lua script using concatenation (avoids f-string/format vs Lua {} conflict)."""
    aggro = str(15.0 if genre in ("dungeon", "horror") else 10.0)
    idx   = str(index)
    return (
        "--[[\n  VoxelForge Enemy AI #" + idx + "\n"
        "  Genre: " + genre + "  Class: " + enemy_class + "\n"
        "--]]\n\n"
        "local AGGRO_DIST  = " + aggro + "\n"
        "local SPEED       = 6.0\n"
        "local ATTACK_DIST = 2.5\n"
        "local health      = 30\n"
        'local state       = "patrol"\n'
        "local patrol_timer = 0\n"
        "local patrol_dx, patrol_dy = 1, 0\n\n"
        "function Start()\n"
        '    print("[Enemy#' + idx + '] Spawned")\n'
        "    if not RigidBody.HasRigidBody(self) then\n"
        "        RigidBody.AddRigidBody(self)\n"
        "    end\n"
        "    RigidBody.SetMass(self, 1.0)\n"
        "    RigidBody.SetUseGravity(self, true)\n"
        "    RigidBody.SetIsStatic(self, false)\n"
        "end\n\n"
        "function Update()\n"
        "    local dt = Time.DeltaTime()\n"
        "    patrol_timer = patrol_timer + dt\n"
        "    if health <= 0 then ECS.DestroyEntity(self); return end\n"
        '    local player = ECS.FindEntityByName("player")\n'
        "    if not player then return end\n"
        "    local my_pos = Transform.GetPosition(self)\n"
        "    local pl_pos = Transform.GetPosition(player)\n"
        "    local dx = pl_pos.x - my_pos.x\n"
        "    local dy = pl_pos.y - my_pos.y\n"
        "    local dist = math.sqrt(dx*dx + dy*dy)\n"
        "    if dist < ATTACK_DIST then\n"
        '        state = "attack"\n'
        "        RigidBody.SetVelocity(self, 0, 0, RigidBody.GetVelocity(self).z)\n"
        "    elseif dist < AGGRO_DIST then\n"
        '        state = "chase"\n'
        "        local len = dist > 0 and dist or 1\n"
        "        RigidBody.SetVelocity(self, dx/len*SPEED, dy/len*SPEED,\n"
        "                              RigidBody.GetVelocity(self).z)\n"
        "    else\n"
        '        state = "patrol"\n'
        "        if patrol_timer > 3.0 then\n"
        "            patrol_timer = 0\n"
        "            local dirs = " + "{{1,0},{-1,0},{0,1},{0,-1}}" + "\n"
        "            local dir = dirs[math.random(#dirs)]\n"
        "            patrol_dx, patrol_dy = dir[1], dir[2]\n"
        "        end\n"
        "        RigidBody.SetVelocity(self, patrol_dx*SPEED*0.5,\n"
        "                              patrol_dy*SPEED*0.5,\n"
        "                              RigidBody.GetVelocity(self).z)\n"
        "    end\n"
        "end\n\n"
        "function TakeDamage(amount)\n"
        "    health = health - (amount or 10)\n"
        "end\n\n"
        "function End()\n"
        '    print("[Enemy#' + idx + '] Destroyed")\n'
        "end\n"
    )


def _objective_script(genre: str, collectibles: int) -> str:
    return (
        "--[[\n"
        "  VoxelForge Objective System\n"
        "  Genre: " + genre + "  Collectibles: " + str(collectibles) + "\n"
        "  Auto-generated by VoxelForge GameGenerator\n"
        "--]]\n\n"
        "local total_chests   = " + str(collectibles) + "\n"
        "local opened_chests = 0\n"
        "local win = false\n\n"
        'function Start()\n'
        '    print("[Objective] Find and open " .. total_chests .. " chests to win!")\n'
        'end\n\n'
        'function Update()\n'
        '    if win then return end\n'
        '    local player = ECS.FindEntityByName("player")\n'
        '    if not player then return end\n'
        '    local pl_pos = Transform.GetPosition(player)\n'
        '    local chests = ECS.FindEntitiesByNameContaining("chest")\n'
        '    for _, chest in ipairs(chests or {}) do\n'
        '        local cp = Transform.GetPosition(chest)\n'
        '        local dx = pl_pos.x - cp.x\n'
        '        local dy = pl_pos.y - cp.y\n'
        '        if math.sqrt(dx*dx + dy*dy) < 3.0 and Input.GetKeyDown("E") then\n'
        '            ECS.DestroyEntity(chest)\n'
        '            opened_chests = opened_chests + 1\n'
        '            print("[Objective] " .. opened_chests .. "/" .. total_chests)\n'
        '            if opened_chests >= total_chests then\n'
        '                win = true\n'
        '                print("[Objective] YOU WIN!")\n'
        '            end\n'
        '        end\n'
        '    end\n'
        '    UI.DrawText("Chests: "..opened_chests.."/"..total_chests, 20, 80, 1,0.9,0.1,1)\n'
        'end\n'
    )


