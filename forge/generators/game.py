"""
forge.generators.game
======================
Complete mini-game generator for VoxelForge.

Given a game concept (genre, theme, size) this module generates:
  - All voxel assets (terrain / dungeon, buildings, characters, props)
  - A complete VoxelForge scene file wiring them all together
  - Lua game scripts (player controller, enemy AI, objective system)
  - A game manifest JSON describing every asset and script

The output is a fully playable mini-game that can be loaded in the
VoxelForge engine with a single command.

Example
-------
>>> from forge.generators.game import GameGenerator
>>> from forge.voxel import Palette
>>> gen = GameGenerator(Palette.natural(), seed=1)
>>> manifest = gen.generate(
...     title       = "Crystal Dungeon",
...     genre       = "dungeon",
...     theme       = "ice",
...     player_class= "mage",
...     enemies     = 3,
... )
>>> print(manifest["scene_path"])
>>> print(manifest["run_command"])
"""

